    private boolean validarUsuario(String nombre, String clave){
        Usuario administrador = banco.getUsuarios().get(0);
        if(nombre.equals(administrador.getNombre()) && clave.toString().equals(administrador.getClave())){
            Principal principal = new Principal();
            principal.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Usuario o clave incorrecta");
        }
        
        return false;
    } 
